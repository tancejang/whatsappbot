class Donor {
    setName(name){
        this.name = name;
    }
    setGender(gender){
        this.gender = gender;
    }
    setAge(age){
        this.age = age;
    }
    setBodyWeight(bodyWeight){
        this.bodyWeight = bodyWeight;
    }
    setPregnantBefore(pregnantBefore){
        this.pregnantBefore = pregnantBefore;
    }
    setCity(city){
        this.city = city;
    }
    setProvince(province){
        this.province = province;
    }
    setBloodType(bloodType){
        this.bloodType = bloodType;
    }
    setRhesus(rhesus){
        this.rhesus = rhesus;
    }
    setCuredDate(curedDate){
        this.curedDate = curedDate;
    }
    setLastDonor(lastDonor){
        this.lastDonor = lastDonor;
    }
}

module.exports = Donor;